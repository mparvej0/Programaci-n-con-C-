#include <iostream>
using namespace std;

int main() {
    //while loop
    int n = 17;
    int count = 1;
    while(count <= n) {
        cout << count << " ";
        count++;
    }
    cout << endl;

    //for loop
    int n1 = 7;
    for(int i=1; i<=n1; i++) {
        cout << i << " ";
    }

    //do while loop
    return 0;
}